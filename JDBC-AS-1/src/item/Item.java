package item;

public class Item {
	private String name;
	private int id;
	private int qis;
	private double price;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getQis() {
		return qis;
	}
	public void setQis(int qis) {
		this.qis = qis;
	}
	public double getPrice() {
		return price;
	}
	public void setPrice(double price) {
		this.price = price;
	}
	
}
